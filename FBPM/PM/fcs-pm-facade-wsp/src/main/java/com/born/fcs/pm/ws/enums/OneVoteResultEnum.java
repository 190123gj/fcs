package com.born.fcs.pm.ws.enums;

/**
 * 董事长一票权
 *
 * @author wuzj
 */
public enum OneVoteResultEnum {
	
	PASS("PASS", "同意"),
	NO_PASS("NO_PASS", "一票否决"),
	RE_COUNCIL("RE_COUNCIL", "复议"), ;
	
	/** 枚举值 */
	private final String code;
	
	/** 枚举描述 */
	private final String message;
	
	/**
	 *
	 * @param code 枚举值
	 * @param message 枚举描述
	 */
	private OneVoteResultEnum(String code, String message) {
		this.code = code;
		this.message = message;
	}
	
	/**
	 * @return Returns the code.
	 */
	public String getCode() {
		return code;
	}
	
	/**
	 * @return Returns the message.
	 */
	public String getMessage() {
		return message;
	}
	
	/**
	 * @return Returns the code.
	 */
	public String code() {
		return code;
	}
	
	/**
	 * @return Returns the message.
	 */
	public String message() {
		return message;
	}
	
	/**
	 * 通过枚举<code>code</code>获得枚举
	 *
	 * @param code
	 * @return OneVoteResultEnum
	 */
	public static OneVoteResultEnum getByCode(String code) {
		for (OneVoteResultEnum _enum : values()) {
			if (_enum.getCode().equals(code)) {
				return _enum;
			}
		}
		return null;
	}
	
	/**
	 * 获取全部枚举
	 * 
	 * @return List<OneVoteResultEnum>
	 */
	public static java.util.List<OneVoteResultEnum> getAllEnum() {
		java.util.List<OneVoteResultEnum> list = new java.util.ArrayList<OneVoteResultEnum>(
			values().length);
		for (OneVoteResultEnum _enum : values()) {
			list.add(_enum);
		}
		return list;
	}
	
	/**
	 * 获取全部枚举值
	 * 
	 * @return List<String>
	 */
	public static java.util.List<String> getAllEnumCode() {
		java.util.List<String> list = new java.util.ArrayList<String>(values().length);
		for (OneVoteResultEnum _enum : values()) {
			list.add(_enum.code());
		}
		return list;
	}
	
	/**
	 * 通过code获取msg
	 * @param code 枚举值
	 * @return
	 */
	public static String getMsgByCode(String code) {
		if (code == null) {
			return null;
		}
		OneVoteResultEnum _enum = getByCode(code);
		if (_enum == null) {
			return null;
		}
		return _enum.getMessage();
	}
	
	/**
	 * 获取枚举code
	 * @param _enum
	 * @return
	 */
	public static String getCode(OneVoteResultEnum _enum) {
		if (_enum == null) {
			return null;
		}
		return _enum.getCode();
	}
}
