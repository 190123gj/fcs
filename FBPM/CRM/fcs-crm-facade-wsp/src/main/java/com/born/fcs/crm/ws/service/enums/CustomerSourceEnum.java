package com.born.fcs.crm.ws.service.enums;

/**
 * 客户来源
 * */
public enum CustomerSourceEnum {
	GRFZ("GRFZ", "个人发展"),
	YXPT("YXPT", "营销平台"),
	QDTJ("QDTJ", "渠道推荐");
	
	/** 枚举值 */
	private final String code;
	
	/** 枚举描述 */
	private final String message;
	
	/**
	 * 
	 * @param code 枚举值
	 * @param message 枚举描述
	 */
	private CustomerSourceEnum(String code, String message) {
		this.code = code;
		this.message = message;
	}
	
	/**
	 * @return Returns the code.
	 */
	public String getCode() {
		return code;
	}
	
	/**
	 * @return Returns the message.
	 */
	public String getMessage() {
		return message;
	}
	
	/**
	 * @return Returns the code.
	 */
	public String code() {
		return code;
	}
	
	/**
	 * @return Returns the message.
	 */
	public String message() {
		return message;
	}
	
	/**
	 * 通过枚举<code>code</code>获得枚举
	 * 
	 * @param code
	 * @return CustomerSourceEnum
	 */
	public static CustomerSourceEnum getByCode(String code) {
		for (CustomerSourceEnum _enum : values()) {
			if (_enum.getCode().equals(code)) {
				return _enum;
			}
		}
		return null;
	}
	
	/**
	 * 获取全部枚举
	 * 
	 * @return List<CustomerSourceEnum>
	 */
	public static java.util.List<CustomerSourceEnum> getAllEnum() {
		java.util.List<CustomerSourceEnum> list = new java.util.ArrayList<CustomerSourceEnum>(
			values().length);
		for (CustomerSourceEnum _enum : values()) {
			list.add(_enum);
		}
		return list;
	}
	
	/**
	 * 获取全部枚举值
	 * 
	 * @return List<String>
	 */
	public static java.util.List<String> getAllEnumCode() {
		java.util.List<String> list = new java.util.ArrayList<String>(values().length);
		for (CustomerSourceEnum _enum : values()) {
			list.add(_enum.code());
		}
		return list;
	}
	
	/**
	 * 通过code获取msg
	 * @param code 枚举值
	 * @return
	 */
	public static String getMsgByCode(String code) {
		if (code == null) {
			return null;
		}
		CustomerSourceEnum _enum = getByCode(code);
		if (_enum == null) {
			return null;
		}
		return _enum.getMessage();
	}
	
	/**
	 * 获取枚举code
	 * @param _enum
	 * @return
	 */
	public static String getCode(CustomerSourceEnum _enum) {
		if (_enum == null) {
			return null;
		}
		return _enum.getCode();
	}
}
