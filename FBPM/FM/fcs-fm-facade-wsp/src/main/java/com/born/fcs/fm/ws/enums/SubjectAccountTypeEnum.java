package com.born.fcs.fm.ws.enums;

/**
 * 
 * 资金类型
 * @author hjiajie
 * 
 */
public enum SubjectAccountTypeEnum {
	
	/** 活期 */
	CURRENT("CURRENT", "活期"),
	
	/** 定期 */
	REGULAR("REGULAR", "定期"),
	
	/** 结构性存库 */
	STRUCTURAL("STRUCTURAL", "结构性存款"),
	
	;
	
	/** 枚举值 */
	private final String code;
	
	/** 枚举描述 */
	private final String message;
	
	/**
	 * 
	 * @param code 枚举值
	 * @param message 枚举描述
	 */
	private SubjectAccountTypeEnum(String code, String message) {
		this.code = code;
		this.message = message;
	}
	
	/**
	 * @return Returns the code.
	 */
	public String getCode() {
		return code;
	}
	
	/**
	 * @return Returns the message.
	 */
	public String getMessage() {
		return message;
	}
	
	/**
	 * @return Returns the code.
	 */
	public String code() {
		return code;
	}
	
	/**
	 * @return Returns the message.
	 */
	public String message() {
		return message;
	}
	
	/**
	 * 通过枚举<code>code</code>获得枚举
	 * 
	 * @param code
	 * @return SubjectAccountTypeEnum
	 */
	public static SubjectAccountTypeEnum getByCode(String code) {
		for (SubjectAccountTypeEnum _enum : values()) {
			if (_enum.getCode().equals(code)) {
				return _enum;
			}
		}
		return null;
	}
	
	/**
	 * 获取全部枚举
	 * 
	 * @return List<SubjectAccountTypeEnum>
	 */
	public static java.util.List<SubjectAccountTypeEnum> getAllEnum() {
		java.util.List<SubjectAccountTypeEnum> list = new java.util.ArrayList<SubjectAccountTypeEnum>(
			values().length);
		for (SubjectAccountTypeEnum _enum : values()) {
			list.add(_enum);
		}
		return list;
	}
	
	/**
	 * 获取全部枚举值
	 * 
	 * @return List<String>
	 */
	public static java.util.List<String> getAllEnumCode() {
		java.util.List<String> list = new java.util.ArrayList<String>(values().length);
		for (SubjectAccountTypeEnum _enum : values()) {
			list.add(_enum.code());
		}
		return list;
	}
	
	/**
	 * 通过code获取msg
	 * @param code 枚举值
	 * @return
	 */
	public static String getMsgByCode(String code) {
		if (code == null) {
			return null;
		}
		SubjectAccountTypeEnum _enum = getByCode(code);
		if (_enum == null) {
			return null;
		}
		return _enum.getMessage();
	}
	
	/**
	 * 获取枚举code
	 * @param _enum
	 * @return
	 */
	public static String getCode(SubjectAccountTypeEnum _enum) {
		if (_enum == null) {
			return null;
		}
		return _enum.getCode();
	}
}
