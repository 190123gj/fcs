package com.born.fcs.fm.ws.enums;

/**
 * 
 * 收款/付款单据处理状态
 * @author wuzj
 * 
 */
public enum ReceiptPaymentFormStatusEnum {
	
	NOT_PROCESS("NOT_PROCESS", "待处理"),
	
	IN_PROCESS("IN_PROCESS", "处理中"),
	
	PROCESSED("PROCESSED", "已处理"), ;
	
	/** 枚举值 */
	private final String code;
	
	/** 枚举描述 */
	private final String message;
	
	/**
	 * 
	 * @param code 枚举值
	 * @param message 枚举描述
	 */
	private ReceiptPaymentFormStatusEnum(String code, String message) {
		this.code = code;
		this.message = message;
	}
	
	/**
	 * @return Returns the code.
	 */
	public String getCode() {
		return code;
	}
	
	/**
	 * @return Returns the message.
	 */
	public String getMessage() {
		return message;
	}
	
	/**
	 * @return Returns the code.
	 */
	public String code() {
		return code;
	}
	
	/**
	 * @return Returns the message.
	 */
	public String message() {
		return message;
	}
	
	/**
	 * 通过枚举<code>code</code>获得枚举
	 * 
	 * @param code
	 * @return VoucherStatusEnum
	 */
	public static ReceiptPaymentFormStatusEnum getByCode(String code) {
		for (ReceiptPaymentFormStatusEnum _enum : values()) {
			if (_enum.getCode().equals(code)) {
				return _enum;
			}
		}
		return null;
	}
	
	/**
	 * 获取全部枚举
	 * 
	 * @return List<VoucherStatusEnum>
	 */
	public static java.util.List<ReceiptPaymentFormStatusEnum> getAllEnum() {
		java.util.List<ReceiptPaymentFormStatusEnum> list = new java.util.ArrayList<ReceiptPaymentFormStatusEnum>(
			values().length);
		for (ReceiptPaymentFormStatusEnum _enum : values()) {
			list.add(_enum);
		}
		return list;
	}
	
	/**
	 * 获取全部枚举值
	 * 
	 * @return List<String>
	 */
	public static java.util.List<String> getAllEnumCode() {
		java.util.List<String> list = new java.util.ArrayList<String>(values().length);
		for (ReceiptPaymentFormStatusEnum _enum : values()) {
			list.add(_enum.code());
		}
		return list;
	}
	
	/**
	 * 通过code获取msg
	 * @param code 枚举值
	 * @return
	 */
	public static String getMsgByCode(String code) {
		if (code == null) {
			return null;
		}
		ReceiptPaymentFormStatusEnum _enum = getByCode(code);
		if (_enum == null) {
			return null;
		}
		return _enum.getMessage();
	}
	
	/**
	 * 获取枚举code
	 * @param _enum
	 * @return
	 */
	public static String getCode(ReceiptPaymentFormStatusEnum _enum) {
		if (_enum == null) {
			return null;
		}
		return _enum.getCode();
	}
}
