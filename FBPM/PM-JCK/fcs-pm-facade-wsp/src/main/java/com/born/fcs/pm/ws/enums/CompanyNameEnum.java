package com.born.fcs.pm.ws.enums;

/**
 * 公司类别[暂用于信汇]
 * 
 * @author hjiajie
 * 
 */
public enum CompanyNameEnum {
	
	
	/** 母公司 */
	NORMAL("NORMAL", "母公司"),
	
	/** 信汇 */
	XINHUI("XINHUI", "信汇"),
	
	;
	
	/** 枚举值 */
	private final String code;
	
	/** 枚举描述 */
	private final String message;
	
	/**
	 * 
	 * @param code 枚举值
	 * @param message 枚举描述
	 */
	private CompanyNameEnum(String code, String message) {
		this.code = code;
		this.message = message;
	}
	
	/**
	 * @return Returns the code.
	 */
	public String getCode() {
		return code;
	}
	
	/**
	 * @return Returns the message.
	 */
	public String getMessage() {
		return message;
	}
	
	/**
	 * @return Returns the code.
	 */
	public String code() {
		return code;
	}
	
	/**
	 * @return Returns the message.
	 */
	public String message() {
		return message;
	}
	
	/**
	 * 通过枚举<code>code</code>获得枚举
	 * 
	 * @param code
	 * @return CompanyNameEnum
	 */
	public static CompanyNameEnum getByCode(String code) {
		for (CompanyNameEnum _enum : values()) {
			if (_enum.getCode().equals(code)) {
				return _enum;
			}
		}
		return null;
	}
	
	/**
	 * 获取全部枚举
	 * 
	 * @return List<CompanyNameEnum>
	 */
	public static java.util.List<CompanyNameEnum> getAllEnum() {
		java.util.List<CompanyNameEnum> list = new java.util.ArrayList<CompanyNameEnum>(
			values().length);
		for (CompanyNameEnum _enum : values()) {
			list.add(_enum);
		}
		return list;
	}
	
	/**
	 * 获取全部枚举值
	 * 
	 * @return List<String>
	 */
	public static java.util.List<String> getAllEnumCode() {
		java.util.List<String> list = new java.util.ArrayList<String>(values().length);
		for (CompanyNameEnum _enum : values()) {
			list.add(_enum.code());
		}
		return list;
	}
	
	/**
	 * 通过code获取msg
	 * @param code 枚举值
	 * @return
	 */
	public static String getMsgByCode(String code) {
		if (code == null) {
			return null;
		}
		CompanyNameEnum _enum = getByCode(code);
		if (_enum == null) {
			return null;
		}
		return _enum.getMessage();
	}
	
	/**
	 * 获取枚举code
	 * @param _enum
	 * @return
	 */
	public static String getCode(CompanyNameEnum _enum) {
		if (_enum == null) {
			return null;
		}
		return _enum.getCode();
	}
}
